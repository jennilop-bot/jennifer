# Ejercicio 2: Calificación Académica
calificacion = 75
if calificacion >= 90:
    letra = "A"
elif calificacion >= 80:
    letra = "B"
elif calificacion >= 70:
    letra = "C"
elif calificacion >= 60:
    letra = "D"
else:
    letra = "F"
print("Calificación:", letra, "(", calificacion, "puntos)")
